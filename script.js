// ==================== بەڕێوەبردنی کاتی تایمەرەکە ====================
let targetDate = new Date("2026-08-01T00:00:00").getTime();

function updateCountdown(){
  let now = new Date().getTime();
  let diff = targetDate - now;

  if(diff < 0) diff = 0;

  document.getElementById("days").innerText = Math.floor(diff/(1000*60*60*24));
  document.getElementById("hours").innerText = Math.floor((diff/(1000*60*60))%24);
  document.getElementById("minutes").innerText = Math.floor((diff/(1000*60))%60);
  document.getElementById("seconds").innerText = Math.floor((diff/1000)%60);
  document.getElementById("ms").innerText = diff % 1000;
}

let countdownInterval = setInterval(updateCountdown, 50);
updateCountdown();


// ==================== بارینی چەکەکان لە پشتەوە (BACKGROUND FALLING) ====================
const container = document.getElementById('fallingContainer');
const gunIconsList = ['︻╦╤─', '▄︻┻═┳', '︻╦̵̵͇̿̿̿̿╤──', '⌐╦╦═─', '︻╦̵̵͇̿̿̿̿══╤─'];
let effectsEnabled = true;
let effectIntervalTime = 800; 
let effectSpeedClass = 'medium';
let spawnInterval;

function createFallingItem() {
  if (!effectsEnabled) return;
  
  const item = document.createElement('div');
  item.classList.add('falling-item', 'fall-animation');
  
  const isText = Math.random() > 0.45;
  if (isText) {
    item.classList.add('text-style');
    item.innerText = 'Kurdish Royal';
    item.style.fontSize = (Math.random() * 8 + 24) + 'px';
  } else {
    const iconSpan = document.createElement('span');
    iconSpan.classList.add('gun-icon');
    iconSpan.innerText = gunIconsList[Math.floor(Math.random() * gunIconsList.length)];
    iconSpan.style.fontSize = (Math.random() * 12 + 30) + 'px';
    item.appendChild(iconSpan);
  }
  
  if(Math.random() > 0.5) item.classList.add('red-theme');

  item.style.left = Math.random() * 95 + 'vw';
  
  // ڕێکخستنی خێرایی جوڵەکە بەپێی ویستی بەکارهێنەر
  let duration = Math.random() * 6 + 6; // default medium
  if (effectSpeedClass === 'slow') duration = Math.random() * 8 + 10;
  if (effectSpeedClass === 'fast') duration = Math.random() * 3 + 4;
  
  item.style.animationDuration = duration + 's';
  container.appendChild(item);
  
  setTimeout(() => { item.remove(); }, 14000);
}

function startFallingEffect() {
  clearInterval(spawnInterval);
  if (!effectsEnabled) return;
  spawnInterval = setInterval(createFallingItem, effectIntervalTime);
}
startFallingEffect();


// ==================== لۆجیکی بەشی ڕێکخستنەکان (SETTINGS CONTROLS) ====================
const openSettingsBtn = document.getElementById("openSettingsBtn");
const closeSettingsBtn = document.getElementById("closeSettingsBtn");
const settingsOverlay = document.getElementById("settingsOverlay");

// کردنەوە و داخستنی پەڕەی ڕێکخستنەکان
openSettingsBtn.addEventListener("click", () => {
  settingsOverlay.classList.add("active");
  playClickSound();
});
closeSettingsBtn.addEventListener("click", () => {
  settingsOverlay.classList.remove("active");
  playClickSound();
});
settingsOverlay.addEventListener("click", (e) => {
  if(e.target === settingsOverlay) settingsOverlay.classList.remove("active");
});

// 1. گۆڕینی زمان لە ڕێکخستنەکانەوە
document.getElementById("settingLanguage").addEventListener("change", (e) => {
  changeLanguage(e.target.value);
  playClickSound();
});

// 2. گۆڕینی ڕوکار (Theme)
document.getElementById("settingTheme").addEventListener("change", (e) => {
  document.body.className = "theme-" + e.target.value;
  playClickSound();
});

// 3. چالاک/ناچالاککردنی بارینی چەکەکان
document.getElementById("settingEffects").addEventListener("change", (e) => {
  effectsEnabled = e.target.checked;
  if(!effectsEnabled) {
    container.innerHTML = "";
  } else {
    startFallingEffect();
  }
  playClickSound();
});

// 4. خێرایی/چڕی بارین
document.getElementById("settingSpeed").addEventListener("change", (e) => {
  effectSpeedClass = e.target.value;
  if(effectSpeedClass === 'slow') effectIntervalTime = 1400;
  if(effectSpeedClass === 'medium') effectIntervalTime = 800;
  if(effectSpeedClass === 'fast') effectIntervalTime = 400;
  startFallingEffect();
  playClickSound();
});

// 5. گۆڕینی قەبارەی فۆنت و نووسینەکان
document.getElementById("settingTextSize").addEventListener("input", (e) => {
  document.getElementById("lang-desc-box").style.fontSize = e.target.value + "px";
  document.querySelector(".big-description").style.fontSize = (parseInt(e.target.value) + 4) + "px";
});

// 6. شاردنەوە یان پیشاندانی ملی چرکە
document.getElementById("settingShowMS").addEventListener("change", (e) => {
  document.getElementById("ms").style.display = e.target.checked ? "block" : "none";
  playClickSound();
});

